    private void BReconocerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BReconocerActionPerformed
        automataTxt.setText("");
        String ruta, rutaImagen ;
        if(opc == 0){
            //si no hay ruta de fichero es porque se ha creado el autómata manualmente
            if(rutaTxt.getText().equals("")){
                ruta = crearafd.getRuta();
            }
            else{
                ruta = rutaTxt.getText();
            }
        }
        else{
            //si no hay ruta de fichero es porque se ha creado el autómata manualmente
            if(rutaTxt.getText().equals("")){
                ruta = crearafnd.getRuta();
            }
            else{
                ruta = rutaTxt.getText();
            }
        }
        
        //creo el autómata
        try {
            m.leerFichero(ruta);
            m.crearFichero();
        } catch (IOException ex) {
            Logger.getLogger(Automata.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        try {
            Thread.sleep(100);
        } catch (InterruptedException ex) {
            Logger.getLogger(Automata.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        //insertamos la imagen del autómata
        rutaImagen = m.getOutPath();
        automataTxt.insertIcon(new ImageIcon(rutaImagen));
        
        boolean resultado;
        String cadena = cadenaTxt.getText();
        if(opc == 0){
            afd = m.getAfd();
            resultado = afd.reconocer(cadena);
        }
        else{
            afnd = m.getAfnd();
            resultado = afnd.reconocer(cadena);
        }
        
        cadena = "Cadena de entrada: " + cadena + "\n";
        if(resultado) {
            cadena = cadena + "El autómata reconoció la cadena\n\n";
        } 
        else {
            cadena = cadena + "El autómata no reconoció la cadena\n\n";
        }
        
        resultadoTxt.setText(cadena);
        
    }//GEN-LAST:event_BReconocerActionPerformed

