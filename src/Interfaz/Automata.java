    private void BSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BSeleccionarActionPerformed
         //Creamos el objeto JFileChooser
        JFileChooser fc = new JFileChooser();
        
        //Creamos el filtro
        FileNameExtensionFilter filtro = new FileNameExtensionFilter(".TXT", "txt");
        
        //Le indicamos el filtro
        fc.setFileFilter(filtro);
        
        //Abrimos la ventana, guardamos la opción seleccionada por el usuario
        int seleccion = fc.showOpenDialog(this); //hay que pasarle un JPanel
        
        //Si el usuario pincha en aceptar
        if(seleccion == JFileChooser.APPROVE_OPTION){
            //Seleccionamos el fichero
            fichero = fc.getSelectedFile();
            //Escribe la ruta del fichero seleccionado en el campo de texto
            rutaTxt.setText(fichero.getAbsolutePath());                
        }
    }//GEN-LAST:event_BSeleccionarActionPerformed

