    private void BAddTransicionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAddTransicionActionPerformed
        //agregamos las transiciones
        int origen = Integer.parseInt(origenTxt.getText());
        char simbolo = simboloTxt.getText().charAt(0);
        String[] destinos = destinosTxt.getText().split(",");
        
        //Fichero automata
        try {
            salidaAutomata.write(origen + " " + simbolo + " ");
            int i;
            for(i=0; i<destinos.length-1; i++){
                salidaAutomata.write(destinos[i] + " ");
            }
            salidaAutomata.write(destinos[i] + "\n");
        } catch (IOException ex) {
            Logger.getLogger(CrearAFND.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        //limpiamos los valores introducidos
        origenTxt.setText("");
        simboloTxt.setText("");
        destinosTxt.setText("");
    }//GEN-LAST:event_BAddTransicionActionPerformed

    private void BAddEstadosFinalesActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAddEstadosFinalesActionPerformed
        String[] finales = estadosFinales.getText().split(",");
        
        //Fichero automata
        try {
            salidaAutomata.write("EF: ");
            int i;
            for(i=0; i<finales.length-1; i++){
                salidaAutomata.write(finales[i] + " ");
            }
            salidaAutomata.write(finales[i] + "\n");
            salidaAutomata.write("T\n");
        } catch (IOException ex) {
            Logger.getLogger(CrearAFND.class.getName()).log(Level.SEVERE, null, ex);
        }

    }//GEN-LAST:event_BAddEstadosFinalesActionPerformed

