    private void BAddTransLambdaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAddTransLambdaActionPerformed
        int origen = Integer.parseInt(origenLambdaTxt.getText());
        String[] destinos = destinosLambdaTxt.getText().split(",");

        //Fichero automata        
        try {
            clicks++;
            if(clicks == 1){
                salidaAutomata.write("L\n");
            }   
            salidaAutomata.write(origen + " ");
            int i;
            for(i=0; i<destinos.length-1; i++){
                salidaAutomata.write(destinos[i] + " ");
            }
            salidaAutomata.write(destinos[i] + "\n");
        } catch (IOException ex) {
            Logger.getLogger(CrearAFND.class.getName()).log(Level.SEVERE, null, ex);
        }

        
        //limpiamos
        origenLambdaTxt.setText("");
        destinosLambdaTxt.setText("");
    }//GEN-LAST:event_BAddTransLambdaActionPerformed

    private void BFinalizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BFinalizarActionPerformed
        try {
            if(clicks == 0){
                salidaAutomata.write("L\n");
            }
            salidaAutomata.write("EOF");
            salidaAutomata.close();
        } catch (IOException ex) {
            Logger.getLogger(CrearAFND.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }//GEN-LAST:event_BFinalizarActionPerformed

